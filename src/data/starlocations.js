/** Lấy màu Bortle dạng hex */
export function getBortleColor(bortleMin) {
  const colors = {
    1: '#3b5bdb',
    2: '#4dabf7',
    3: '#74c0fc',
    4: '#a9e34b',
    5: '#ffd43b',
    6: '#ff922b',
    7: '#f03e3e',
  }
  return colors[bortleMin] || '#adb5bd'
}

/** Format Bortle range string */
export function formatBortle(min, max) {
  if (min === max) return `Bortle ${min}`
  return `Bortle ${min}–${max}`
}

/** Lọc theo region */
export function filterByRegion(region) {
  if (!region) return starLocations
  return starLocations.filter(l => l.region === region)
}

/** Lọc theo Bortle tối đa */
export function filterByMaxBortle(maxBortle) {
  return starLocations.filter(l => l.bortleMin <= maxBortle)
}

/** Sort theo Bortle (tốt nhất lên đầu) */
export function sortByBortle(locations) {
  return [...locations].sort((a, b) => a.bortleMin - b.bortleMin)
}

export default starLocations